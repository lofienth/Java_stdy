//Постановка задачи:
//        Определить матрицу (двумерный массив) и ее заполнить случайными значениями.
//        Построить вектор В, которой возвращает – 2) среднее арифметическое положительных элементов в i-м столбце

import java.util.Random;

public class Main {
    public static void main(String[] args) {

        int rows = 4; //количество строк
        int cols = 5; //количество столбцов

        int[][] matrix = new int[rows][cols]; //создание матрицы

        Random random = new Random(); //создание объекта для генерации случайных чисел

        //заполнение матрицы случайными числами
        for (int i = 0; i < rows; i++){     // проход по строкам
            for (int j = 0; j < cols; j++){     // проход по столбцам
                matrix[i][j] = random.nextInt(101) - 50;    // числа от -50 до 50
            }
        }

        //вывод исходной матрицы на экран
        for (int i = 0; i < rows; i++){
            for (int j = 0; j < cols; j++){
                System.out.print(matrix[i][j] + "\t"); //вывод с табуляцией
            }
            System.out.println(); // переход на новую строку после вывода одной строки матрицы
        }

        //вектор B
        double[] B = new double[cols];

        //обработка каждого столбца отдельно
        for (int j = 0; j < cols; j++){

            //переменная для суммы положительных элементов текущего столбца
            int sum = 0;

            //переменная для подсчета количества положительных элементов
            int count = 0;

            //проход по всем строкам текущего столбца
            for (int i = 0; i < rows; i++){

                //если элемент положительный, учитываем его
                if (matrix[i][j] > 0){
                    sum = sum + matrix[i][j];
                    count++;
                }
            }

            //если в столбце есть положительные элементы,
            //находим их среднее арифметическое
            if (count > 0){
                B[j] = (double) sum / count;
            }else{
                //если положительных элементов нет, записываем 0
                B[j] = 0;
            }

        }

        System.out.println();
        System.out.println("Вектор B:");
        for (int j = 0; j < cols; j++){
            //вывод вектора B с сокращением дробной части чисел до тысячной доли
            System.out.print(Math.round(B[j] * 1000.0) / 1000.0 + "\t");
        }
    }
}